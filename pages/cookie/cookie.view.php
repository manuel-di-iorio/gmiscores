  <h2>Cosa sono i cookie?</h2>
  <p>I cookie sono piccoli file di testo che i siti visitati dagli utenti inviano ai loro terminali, dove vengono
    memorizzati per essere poi ritrasmessi agli stessi siti alla successiva visita. I cookie delle cosiddette "terze
    parti" vengono, invece, impostati da un sito web diverso da quello che l'utente sta visitando. Questo perché su ogni
    sito possono essere presenti elementi (immagini, mappe, suoni, specifici link a pagine web di altri domini, ecc.)
    che risiedono su server diversi da quello del sito visitato.</p>

  <h2>Tipologie di cookie utilizzate da questo sito</h2>
  <p>Questo sito utilizza esclusivamente <strong>cookie tecnici</strong>. Questi cookie sono essenziali per consentirti
    di navigare nel sito e utilizzare le sue funzionalità. Senza questi cookie, alcuni servizi non potrebbero essere
    forniti.</p>
  <p>In particolare, utilizziamo:</p>
  <ul>
    <li><strong>Cookie di sessione:</strong> Questi cookie sono temporanei e vengono eliminati automaticamente quando
      chiudi il browser. Sono utilizzati per mantenere lo stato della tua sessione (ad esempio, se sei loggato).</li>
    <li><strong>Cookie persistenti (per preferenze):</strong> Potremmo utilizzare cookie persistenti per ricordare le
      tue preferenze (come la preferenza del tema chiaro/scuro). Questi cookie rimangono sul tuo dispositivo per un
      periodo di tempo più lungo o finché non li elimini manualmente.</li>
  </ul>

  <h2>Cookie di terze parti</h2>
  <p><strong>Non utilizziamo cookie di profilazione di terze parti.</strong></p>
  <p>Il nostro sito potrebbe includere collegamenti a siti web di terze parti o servizi che potrebbero impostare i
    propri cookie. Ad esempio, potremmo incorporare video da YouTube o link a social network. Questi siti di terze parti
    hanno le proprie informative sulla privacy e sui cookie, che ti invitiamo a consultare.</p>
  <p>Per quanto riguarda Google Analytics, se utilizzato, è configurato per anonimizzare il tuo indirizzo IP (come
    indicato nella configurazione di Google Analytics nel file `layout.php`: `gtag('config',
    '<?= $config["analyticsId"] ?>', { 'anonymize_ip': true });`). Questo significa che Google non sarà in grado di
    associare il tuo indirizzo IP completo ad altri dati posseduti da Google.</p>

